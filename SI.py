# program to find simple interest for the given p,n,r
p=int(input("enter the Principle interest:")) #get the principle value
n=int(input("enter the Time(N):"))            #get the Time value
r=int(input("Enter the rate of interest:"))   #get the rate of interest

SI=(p*n*r)/100 #formula to find the simple interest

print("The simple Interest is",SI)# to display the output if the SI
